// --- php (apache; mod_php serves HTTP on :80, apache drops to www-data) ---

type phpBuilder struct{}

func (phpBuilder) Name() string                  { return "php" }
func (phpBuilder) Detect(f map[string]bool) bool { return f["composer.json"] || f["index.php"] }
func (phpBuilder) Dockerfile(s Spec) (string, error) {
	ver, err := version(s.Version, "8.3")
	if err != nil {
		return "", err
	}
	pkgs, err := packageLine("apt", s.Packages)
	if err != nil {
		return "", err
	}
	envs, err := envLines(s.Env)
	if err != nil {
		return "", err
	}
	install, err := shellLine("build.install", s.Install) // e.g. composer install
	if err != nil {
		return "", err
	}
	build, err := shellLine("build.build", s.Build)
	if err != nil {
		return "", err
	}
	lines := []string{
		"# generated by Helmsman — do not edit",
		fmt.Sprintf("FROM php:%s-apache", ver),
		"WORKDIR /var/www/html",
		pkgs,
		"COPY --from=composer:2 /usr/bin/composer /usr/bin/composer",
	}
	lines = append(lines, envs...)
	lines = append(lines, "COPY . .", runIf(install), runIf(build))
	// apache starts as root and drops to www-data itself; an explicit USER would
	// break binding :80, so php is the documented exception to USER app.
	return join(lines...), nil
}

// --- static (nginx serving the repo, optional build step) ---

type staticBuilder struct{}

func (staticBuilder) Name() string                  { return "static" }
func (staticBuilder) Detect(f map[string]bool) bool { return f["index.html"] }
func (staticBuilder) Dockerfile(s Spec) (string, error) {
	envs, err := envLines(s.Env)
	if err != nil {
		return "", err
	}
	build, err := shellLine("build.build", s.Build)
	if err != nil {
		return "", err
	}
	install, err := shellLine("build.install", s.Install)
	if err != nil {
		return "", err
	}
	// If there's a build step, run it in a node stage then serve the result; else
	// serve the repo as-is. (For a non-default output dir, use language: generic.)
	if build != "" || install != "" {
		ver, _ := version(s.Version, "20")
		lines := []string{
			"# generated by Helmsman — do not edit",
			fmt.Sprintf("FROM node:%s-alpine AS build", ver),
			"WORKDIR /app",
		}
		lines = append(lines, envs...)
		lines = append(lines, "COPY . .", runIf(install), runIf(build),
			"FROM nginx:alpine",
			"COPY --from=build /app /usr/share/nginx/html",
		)
		return join(lines...), nil
	}
	return join(
		"# generated by Helmsman — do not edit",
		"FROM nginx:alpine",
		"COPY . /usr/share/nginx/html",
	), nil
}

// --- generic (best effort: operator's base + commands) ---

type genericBuilder struct{}

func (genericBuilder) Name() string                { return "generic" }
func (genericBuilder) Detect(map[string]bool) bool { return false } // never auto-selected
func (genericBuilder) Dockerfile(s Spec) (string, error) {
	if err := validImage(s.Base); err != nil {
		return "", err
	}
	pkgs, err := packageLine("apt", s.Packages) // best-effort: assume a debian-ish base
	if err != nil {
		return "", err
	}
	envs, err := envLines(s.Env)
	if err != nil {
		return "", err
	}
	install, err := shellLine("build.install", s.Install)
	if err != nil {
		return "", err
	}
	build, err := shellLine("build.build", s.Build)
	if err != nil {
		return "", err
	}
	cmd, err := cmdLine(s.Start)
	if err != nil {
		return "", err
	}
	lines := []string{"# generated by Helmsman — do not edit", "FROM " + s.Base, "WORKDIR /app"}
	if pkgs != "" {
		lines = append(lines, pkgs)
	}
	lines = append(lines, envs...)
	lines = append(lines, "COPY . .", runIf(install), runIf(build))
	lines = append(lines, nonrootIf(s.Nonroot, nonrootDebian())...)
	lines = append(lines, cmd)
	return join(lines...), nil
}

// --- small helpers ---

func orDefault(v, def string) string {
	if v == "" {
		return def
	}
	return v
}

// runIf returns a RUN line for a non-empty command, else "".
func runIf(cmd string) string {
	if cmd == "" {
		return ""
	}
	return "RUN " + cmd
}

// nonrootIf returns the non-root snippet when enabled, else nil.
func nonrootIf(on bool, snippet []string) []string {
	if !on {
		return nil
	}
	return snippet
}
