// --- go ---

type goBuilder struct{}

func (goBuilder) Name() string                  { return "go" }
func (goBuilder) Detect(f map[string]bool) bool { return f["go.mod"] }
func (goBuilder) Dockerfile(s Spec) (string, error) {
	ver, err := version(s.Version, "1.23")
	if err != nil {
		return "", err
	}
	envs, err := envLines(s.Env)
	if err != nil {
		return "", err
	}
	install, err := shellLine("build.install", orDefault(s.Install, "go mod download"))
	if err != nil {
		return "", err
	}
	// CGO off → a static binary that runs on a tiny base.
	build, err := shellLine("build.build", orDefault(s.Build, "CGO_ENABLED=0 go build -o /out/app ./..."))
	if err != nil {
		return "", err
	}
	cmd := `CMD ["/app/app"]`
	if len(s.Start) > 0 {
		if cmd, err = cmdLine(s.Start); err != nil {
			return "", err
		}
	}
	lines := []string{
		"# generated by Helmsman — do not edit",
		fmt.Sprintf("FROM golang:%s-alpine AS build", ver),
		"WORKDIR /app",
	}
	lines = append(lines, envs...)
	lines = append(lines,
		"COPY . .",
		"RUN "+install,
		"RUN "+build,
		"FROM alpine:3",
		"WORKDIR /app",
		"RUN apk add --no-cache ca-certificates",
		"COPY --from=build /out/app /app/app",
	)
	lines = append(lines, nonrootIf(s.Nonroot, nonrootAlpine())...)
	lines = append(lines, cmd)
	return join(lines...), nil
}

