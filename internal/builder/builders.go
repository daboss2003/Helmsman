package builder

import "fmt"

// Each builder renders a hardened Dockerfile. The operator's install/build commands
// are sanitized to a single line (shellLine) so they can't inject extra directives;
// images run as a non-root user by default. These templates are sane starting points
// — an operator with an unusual build uses language: generic.

// --- node ---

type nodeBuilder struct{}

func (nodeBuilder) Name() string                  { return "node" }
func (nodeBuilder) Detect(f map[string]bool) bool { return f["package.json"] }
func (nodeBuilder) Dockerfile(s Spec) (string, error) {
	ver, err := version(s.Version, "20")
	if err != nil {
		return "", err
	}
	base := fmt.Sprintf("node:%s-alpine", ver)
	pkgs, err := packageLine("apk", s.Packages)
	if err != nil {
		return "", err
	}
	envs, err := envLines(s.Env)
	if err != nil {
		return "", err
	}
	install, err := shellLine("build.install", orDefault(s.Install, "npm install"))
	if err != nil {
		return "", err
	}
	build, err := shellLine("build.build", s.Build)
	if err != nil {
		return "", err
	}
	cmd, err := cmdLine(s.Start)
	if err != nil {
		return "", err
	}
	lines := []string{
		"# generated by Helmsman — do not edit",
		"FROM " + base + " AS build",
		"WORKDIR /app",
		pkgs,
	}
	lines = append(lines, envs...)
	lines = append(lines,
		// Copy manifests + lockfiles first so the install layer caches until they
		// change (a source-only edit won't re-run the install).
		"COPY package*.json ./",
		"RUN "+install,
		"COPY . .",
		runIf(build),
		"FROM "+base+" AS run",
		"WORKDIR /app",
		"COPY --from=build /app /app",
	)
	lines = append(lines, nonrootIf(s.Nonroot, nonrootAlpine())...)
	lines = append(lines, cmd)
	return join(lines...), nil
}

